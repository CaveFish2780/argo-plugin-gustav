
public class Calculator {

	protected Double add(double num, double num2) {
		return (double)num+num2;
	}
	
	protected Double subtract(double num, double num2) {
		return (double)num-num2;
	}
	
	protected Double multiply(double num, double num2 ) {
		return num*num2;
	}
	
	protected Double divide(double num, double num2) {
		return num/num2;
	}
	
	protected Double square(double num) {
		return num*num;
	}
	
	
}
